#include "libft.h"

void	ft_putendl(const char *s)
{
	int		i;
	char	x;

	i = 0;
	x = '\n';
	if (s == NULL)
		return ;
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		i++;
	}
	write(1, &x, 1);
}
