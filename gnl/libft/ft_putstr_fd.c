#include "libft.h"

void	ft_putstr_fd(const char *s, int fd)
{
	int		i;

	if (s == NULL)
		return ;
	if (fd < 1)
		return ;
	i = 0;
	while (s[i] != '\0')
	{
		write(fd, &s[i], 1);
		i++;
	}
}
