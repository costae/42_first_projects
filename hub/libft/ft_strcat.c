#include "libft.h"

char		*ft_strcat(char *dest, const char *src)
{
	size_t	j;
	size_t	i;

	i = 0;
	j = ft_strlen(dest);
	while (src[i])
	{
		dest[j + i] = src[i];
		i++;
	}
	dest[j + i] = 0;
	return (dest);
}
