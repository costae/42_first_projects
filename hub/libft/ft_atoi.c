#include "libft.h"

int		ft_atoi(const char *str)
{
	int		i;
	int		dst;
	int		sign;

	i = 0;
	dst = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n'
			|| str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	sign = (str[i] == '-') ? -1 : 1;
	i += (str[i] == '+' || str[i] == '-') ? 1 : 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		dst = (dst * 10) + (str[i] - 48);
		i++;
	}
	return (dst * sign);
}
