void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		main(int argc, char **argv)
{
	int i;

	while (argc != 1)
	{
		i = argc - 1;
		ft_putstr(argv[i]);
		ft_putchar('\n');
		argc--;
	}
	return (0);
}
