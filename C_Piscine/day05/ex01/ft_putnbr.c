void	ft_putchar(char c);

void	ft_putnbr(int b)
{
	char			c[20];
	unsigned int	i;

	i = 0;
	if (b < 0)
	{
		ft_putchar('-');
		b = -b;
	}
	if (b == 0)
		ft_putchar('0');
	while (b != 0)
	{
		c[i] = b % 10 + '0';
		i++;
		b /= 10;
	}
	while (i >= 0)
	{
		ft_putchar(c[i - 1]);
		i--;
	}
}
