int			ft_atoi(char *str)
{
	int		a;
	int		i;
	char	c;
	int		n;

	i = 0;
	n = 0;
	a = 0;
	while ((str[i] == '\t') || (str[i] == '\n') || (str[i] == '\f')
			|| (str[i] == ' ') || (str[i] == '\r') || (str[i] == '\v'))
		i++;
	if (str[i] == '-')
		n = 1;
	while (str[i] && (str[i] >= '0') && (str[i] <= '9'))
	{
		a = a * 10;
		a = a + (int)str[i] - '0';
		i++;
	}
	if (n == 1)
		return (-a);
	else
		return (a);
}
