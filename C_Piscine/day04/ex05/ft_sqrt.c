int		ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb <= 0)
		return (0);
	else if (nb == 1)
		return (1);
	else
	{
		while (i < nb)
		{
			if ((nb % i) == 0)
			{
				if ((nb / i) == i)
					return (i);
			}
			i++;
		}
		return (0);
	}
}
